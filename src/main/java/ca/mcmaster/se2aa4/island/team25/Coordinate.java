package ca.mcmaster.se2aa4.island.team25;

public class Coordinate {
    private int x,y; //X and Y coordinates 

    public coodinate (int x, int y){
        this.x = x;
        this.y = y;
    }

    public int getX(){
        return x;
    }

    public int getY(){
        return y;
    }

    public void setX(int x){
        this.x = x;
    }

    public void setY(int y){
        this.y = y;
    }

}